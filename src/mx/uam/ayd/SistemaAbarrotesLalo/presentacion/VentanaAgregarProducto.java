    private void jButtonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarActionPerformed
        //JOptionPane.showMessageDialog (null, "Es necesario llenar los campos");
        int id = Integer.parseInt(jTextFieldId.getText());
        String nombre = jTextFieldNombre.getText(); 
        String marca = jTextFieldMarca.getText();
        double precio = Double.parseDouble(jTextFieldPrecio.getText()); //convierte a doble 
        String caducidad=jTextFieldCaducidad.getText();
        int existencias = Integer.parseInt(jTextFieldExistencias.getText());
            
     try {
         agregarProducto=controlProducto1.agregarProducto(id, nombre, caducidad, existencias, precio, marca);
     } catch (SQLException ex) {
         Logger.getLogger(VentanaAgregarProducto.class.getName()).log(Level.SEVERE, null, ex);
     }
                       
    }//GEN-LAST:event_jButtonAgregarActionPerformed
