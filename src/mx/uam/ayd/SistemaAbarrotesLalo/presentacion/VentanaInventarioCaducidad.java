    void llenaTablaDeProductosACaducar(ResultSet productosACaducar) {
           try{
            DefaultTableModel dt=new DefaultTableModel();
            dt.addColumn("PRODUCTO");
            dt.addColumn("CADUCIDAD");
            while(productosACaducar.next()){      
                Object[] fila=new Object[3];
                fila[0]=productosACaducar.getString(1);
                fila[1]=productosACaducar.getString(2);
                dt.addRow(fila);
            }
            if(dt.getRowCount()<10){
                Object[] fila=new Object[3];
                int numeroDeFilas=(10-dt.getRowCount());
                for(int i=0;i<numeroDeFilas;i++){
                    dt.addRow(fila);
                }
            }
            this.jTable2.setModel(dt);
        }catch(Exception ex){
            
        }
    }
